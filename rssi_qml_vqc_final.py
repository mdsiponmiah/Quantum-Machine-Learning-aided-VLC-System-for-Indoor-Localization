def loss(weights, X, y):
    preds = [model(x, weights) for x in X]
    # Convert expvals to probabilities via sigmoid-like mapping
    probs = (np.array(preds) + 1) / 2.0  # in [0,1]
    # Binary cross-entropy
    eps = 1e-8
    return -np.mean(y * np.log(probs + eps) + (1 - y) * np.log(1 - probs + eps))

