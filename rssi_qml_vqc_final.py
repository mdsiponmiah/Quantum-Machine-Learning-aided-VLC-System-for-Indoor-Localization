import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler, MinMaxScaler
from sklearn.svm import SVR
from sklearn.tree import DecisionTreeRegressor
from sklearn.linear_model import LinearRegression
from sklearn.neighbors import KNeighborsRegressor
from sklearn.metrics import accuracy_score, f1_score

# ---------------------------------------------------------
# 1. Load Dataset
# ---------------------------------------------------------
df = pd.read_csv("/content/drive/My Drive/Datasets_QML/rssiML.csv")

X = df[['A', 'B', 'C', 'D']]
y_class = df['x'].astype(int)   # classification label

# Unique classes (0,5,10,...)
classes = sorted(df['x'].unique())

def to_class(pred):
    """Map continuous prediction to nearest valid class."""
    return min(classes, key=lambda c: abs(c - pred))

# ---------------------------------------------------------
# 2. Train/Test Split
# ---------------------------------------------------------
X_train, X_test, y_train, y_test = train_test_split(
    X, y_class, test_size=0.2, random_state=42
)

# ---------------------------------------------------------
# 3. Scaling (needed for SVM, kNN, LR)
# ---------------------------------------------------------
#scaler = StandardScaler()
scaler = MinMaxScaler()
X_train_scaled = scaler.fit_transform(X_train)
X_test_scaled = scaler.transform(X_test)

# ---------------------------------------------------------
# 4. Define Models (regressors)
# ---------------------------------------------------------
models = {
    "LinearRegression": LinearRegression(),
    "DecisionTree": DecisionTreeRegressor(random_state=42),
    "kNN": KNeighborsRegressor(n_neighbors=5),
    "SVM_RBF": SVR(kernel='rbf', C=100, gamma='scale')
}

# ---------------------------------------------------------
# 5. Train & Evaluate Classification Metrics
# ---------------------------------------------------------
results = {}

for name, model in models.items():

    # DT does NOT use scaling
    if name == "DecisionTree":
        Xtr, Xte = X_train, X_test
    else:
        Xtr, Xte = X_train_scaled, X_test_scaled

    clf = model.__class__(**model.get_params())
    clf.fit(Xtr, y_train)

    # Raw continuous predictions
    y_pred_raw = clf.predict(Xte)

    # Convert to nearest class
    y_pred = np.array([to_class(v) for v in y_pred_raw])

    # Metrics
    acc = accuracy_score(y_test, y_pred)
    f1_macro = f1_score(y_test, y_pred, average="macro")
    f1_weighted = f1_score(y_test, y_pred, average="weighted")

    results[name] = {
        "Accuracy": acc,
        "F1_macro": f1_macro,
        "F1_weighted": f1_weighted
    }

# ---------------------------------------------------------
# 6. Display Results
# ---------------------------------------------------------
print("\n=== Classification Metrics (x as class) ===\n")
for model_name, metrics in results.items():
    print(f"{model_name}:")
    print(f"  Accuracy     = {metrics['Accuracy']:.4f}")
    print(f"  F1 (macro)   = {metrics['F1_macro']:.4f}")
    print(f"  F1 (weighted)= {metrics['F1_weighted']:.4f}\n")