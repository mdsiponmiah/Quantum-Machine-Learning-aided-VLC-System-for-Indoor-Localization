n_qubits = 4
n_layers = 4 #3
dev = qml.device("default.qubit", wires=n_qubits)

def feature_map(x):
    for i in range(n_qubits):
        qml.RY(x[i], wires=i)
    for i in range(n_qubits - 1):
        qml.CNOT(wires=[i, i + 1])

def variational_block(weights):
    # weights shape: (n_layers, n_qubits, 3)
    for l in range(n_layers):
        for i in range(n_qubits):
            qml.RX(weights[l, i, 0], wires=i)
            qml.RY(weights[l, i, 1], wires=i)
            qml.RZ(weights[l, i, 2], wires=i)
        for i in range(n_qubits - 1):
            qml.CNOT(wires=[i, i + 1])

@qml.qnode(dev)
def circuit(x, weights):
    feature_map(x)
    variational_block(weights)
    return qml.expval(qml.PauliZ(0))

def model(x, weights):
    # Map expectation value in [-1,1] to logits per class via a simple linear head
    # For simplicity, we treat this as binary-like and then threshold;
    # for multi-class, you’d typically use one-vs-rest or a small classical head.
    return circuit(x, weights)

